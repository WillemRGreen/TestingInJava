package com.qa.junitexample;

public class Calculator {
	
	public static int addNums(int x, int y) {
		int result = x + y;
		return result;
	}
	
	public static int subtractNums(int x, int y) {
		int result = x - y;
		return result;
	}
	
	public static int multiplyNums(int x, int y) {
		int result = x * y;
		return result;
	}
	
	public static int divideNums(int x, int y) {
		int result = x / y;
		return result;
	}
	
	public static int modNums(int x, int y) {
		int result = x % y;
		return result;
	}

}
